#include <nuttx/config.h>
#include <sys/types.h>
#include <string.h>

/****************************************************************************
 * Global Functions
 ****************************************************************************/

#ifndef CONFIG_ARCH_STRNLEN
size_t strnlen(const char *s, size_t maxlen)
{
  const char *sc;
  for (sc = s; maxlen != 0 && *sc != '\0'; maxlen--, ++sc);
  return sc - s;
}
#endif
